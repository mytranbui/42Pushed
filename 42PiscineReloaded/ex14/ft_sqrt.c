int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (nb > 0 && i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		else if (i >= 46341)
			return (0);
		i++;
	}
	return (0);
}
